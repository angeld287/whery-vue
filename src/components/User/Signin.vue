<template>
  <v-container>
    <v-layout row v-if="error">
      <v-flex xs12 sm6 offset-sm3>
        <app-alert @dismissed="onDismissed" :text="error"></app-alert>
      </v-flex>
    </v-layout>
    <v-layout row>
      <v-flex xs12 sm6 offset-sm3>
        <v-card>
          <v-card-text>
            <v-container>
              <form @submit.prevent="onSignin">
                <v-layout row>
                  <v-flex xs12>
                    <v-text-field
                      name="username"
                      label="Username"
                      id="username"
                      v-model="username"
                      required></v-text-field>
                  </v-flex>
                </v-layout>
                <v-layout row>
                  <v-flex xs12>
                    <v-text-field
                      name="password"
                      label="Password"
                      id="password"
                      v-model="password"
                      type="password"
                      required></v-text-field>
                  </v-flex>
                </v-layout>
                <v-layout row>
                  <v-flex xs12>
                    <v-btn type="submit" :disabled="loading" :loading="loading">
                      Sign in
                       <span slot="loader" class="custom-loader">
                        <v-icon light>cached</v-icon>
                       </span>
                    </v-btn>
                  </v-flex>
                </v-layout>
              </form>
            </v-container>
          </v-card-text>
        </v-card>
      </v-flex>
    </v-layout>
  </v-container>
</template>

<script>
  import Vue from 'vue'
  import axios from 'axios'
  import VueAxios from 'vue-axios'

   var string = `<script>
								location.reload();
							  <\/script>;`
  
  Vue.use(VueAxios, axios)
  export default {
    data () {
      return {
        username: '',
        password: ''
      }
    },
    computed: {
      user () {
       return this.$store.getters.user
      },
      token () {
       return this.$store.getters.user.token
      },
      error () {
       return this.$store.getters.error
      },
      loading () {
        return this.$store.getters.loading
      }
    },
    watch: {
      token (value) {
        if (value !== null && value !== undefined) {
         //this.$router.push('/Profile')
         this.loadScripts().then(() => {
            this.executeScript();
          })
        }
      }
    },
    methods: {
      loadScripts() {
        return new Promise(resolve => {
          
          let scriptEl = document.createElement("script");
          scriptEl.src = "https://cdnjs.cloudflare.com/ajax/libs/howler/2.0.5/howler.js";
          scriptEl.type = "text/javascript";
      
          // Attach script to head
          document.getElementsByTagName("head")[0].appendChild(scriptEl); 
          // Wait for tag to load before promise is resolved     
	      scriptEl.addEventListener('load',() => {
            resolve();
          });
        });
      },
      executeScript() {
        // remove script tags
        let script = string.replace(/<\/?script>/g, "")
        eval(script)
      },
      onSignin(){
        this.$store.dispatch('signUserIn', {username: this.username, password: this.password})
      },
      onDismissed(){
        this.$store.dispatch('clearError')
      }
    }
  }
</script>
